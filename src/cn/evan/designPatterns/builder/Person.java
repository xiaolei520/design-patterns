package cn.evan.designPatterns.builder;

public class Person {

	private String pHead;
	
	private String pBody;
	
	private String pFoot;

	public String getpHead() {
		return pHead;
	}

	public void setpHead(String pHead) {
		this.pHead = pHead;
	}

	public String getpBody() {
		return pBody;
	}

	public void setpBody(String pBody) {
		this.pBody = pBody;
	}

	public String getpFoot() {
		return pFoot;
	}

	public void setpFoot(String pFoot) {
		this.pFoot = pFoot;
	}
	
}
